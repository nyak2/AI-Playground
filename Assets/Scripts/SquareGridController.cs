public class SquareGridController : MonoBehaviour
{
    public SquareGrid squareGrid;

    [SerializeField] CellView cellPrefab;

    private Dictionary<Vector3Int, CellView> cellViewStorage;

    private void Awake()
    {
        squareGrid = new(MapData.Raw, MapData.columsPerRow, true);
        MakeCellViews();
    }

    private void MakeCellViews()
    {
        cellViewStorage = new();

        foreach (var cell in squareGrid.Cells)
        {
            CellView cd = Instantiate(cellPrefab);
            cd.transform.SetParent(this.gameObject.transform);
            cd.SetData(cell.Value);

            cellViewStorage.Add(cell.Value.Coordinate, cd);
        }

    }
    public CellView GetCellView(Vector3Int coord)
        => cellViewStorage[coord];
    public bool TryGetCellData(Vector3Int coord, out ICellData cellData)
        => squareGrid.TryGetCellData(coord, out cellData);
    public bool TryChangeCellData(Vector3Int coord, int cost)
        => squareGrid.TryChangeCellData(coord, cost);

    public ISquareGrid GetSquareGrid() => squareGrid;
}
